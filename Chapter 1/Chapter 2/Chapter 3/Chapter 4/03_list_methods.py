l1 = [1,8,7,2,21,15]
l1.sort() #sorts the list
print(l1)

l1.reverse() #reverses the list
print(l1)

l1.append(45) #adds 45 at the end of the list
print(l1)

l1.insert(4,544) #inserts 544 at index 4
print(l1)

l1.pop(4) #pops out object from index 4
print(l1)

l1.remove(21) #removes the named object
print(l1)